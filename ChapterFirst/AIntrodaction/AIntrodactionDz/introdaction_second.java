
package AIntrodactionDz;

/*
 * Java Developer Kit (JDK) подаёт коммантду компелятору на запуск приложения introdaction_first
 * Компелятор преобразует спецификацию кода Java на байт-код
 * Java Runtime Environment карелирует свои действия Java Virtual Machine(JVM), далее все эти 3 пунка обозначаются как "text"
 * JVM получат команду на обработку класса introdaction_first
 */
class introdaction_first{

    /*
     *  text
     *  JVM получат команду на обработку метода main класса introdaction_first
     */
    public static void main(String[] args)
    {
        /*
        * text
        * JVM получает команду на обработку Sout.out.println
        * далее процесс вывода на экран смотри в документаци к out от Java
        */
        System.out.println("Hi, world");
    }
}